#include "ft_printf.h"

int	ft_putnbr(int nbr)
{
	long	n;

	n = (long) nbr;
	if (nbr < 0)
	{
		n = (long) nbr * -1;
		ft_putchar('-');
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + 48);
	return (ft_ft(nbr));
}
